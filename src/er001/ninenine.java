package er001;

public class ninenine {
	public static void main(String[] args) {
	for (int i = 0; i <= 100; i++) {
		if (i % 4 == 0)
			System.out.print(i + " ");
	}
	
//	System.out.println();
	
	System.out.print("\n");
	
	
	for (int i = 0; i <= 100; i += 4) {
		System.out.print(i + " ");
	}
	
}

}
